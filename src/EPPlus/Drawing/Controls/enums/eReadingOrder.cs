namespace OfficeOpenXml.Drawing.Controls
{
    /// <summary>
    /// The reading order
    /// </summary>
    public enum eReadingOrder
    {
        /// <summary>
        /// Reading order is determined by the first non-whitespace character
        /// </summary>
        ContextDependent = 0,
        /// <summary>
        /// Left to Right
        /// </summary>
        LeftToRight = 1,
        /// <summary>
        /// Right to Left
        /// </summary>
        RightToLeft = 2
    }
}